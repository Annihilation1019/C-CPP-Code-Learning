#include <stdio.h>
int main()
{
    for (size_t i = 0, j = 1; i < 10; i++, j++) //,是序列点，保证i和j的值都会被更新
    {
        /* code */
    }
    // for(;;a++,cost=a*2)此处的","也是序列点，顺序尤为重要，保证左侧的副作用在右侧之前发生，即a先自增，再计算cost
    int x = 0, y = 0, z = 0;
    x = (y = 3, (z = ++y + 2) + 5); // 整个逗号表达式的值是最后一个表达式的值，即z+5，逗号表达式遵循从左到右的顺序
    printf("%d\n", x);
    printf("%d\n", z = 125, 249); // 逗号表达式的值是最后一个表达式的值，即249
    return 0;
}
/*逗号也可做分隔符*/